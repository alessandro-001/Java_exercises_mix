package Fundamentals;

public class LoopExample {

	public static void main(String[] args) {
		
		//for loop:
		for (int i = 0; i < 5; i++) {
			System.out.println("The value of i is: " + i);
		}
		
		//while loop:
		int j = 0;
		while (j < 10) {
			System.out.println("The value of j is: " + j);
			j++;
		}
		
	}

}
